#!/usr/bin/env bun
/**
 * Workaround paraglide-sveltekit generator.
 *
 * The official inlang vite plugin pulls its modules from a CDN; the
 * jsdelivr → data-URI handoff is fragile under bun, so we hand-generate
 * the same shape of paraglide artifacts that the plugin would produce.
 *
 * Reads `packages/i18n/messages/{en,de}.json` and writes:
 *
 *   apps/<app>/src/lib/paraglide/
 *     runtime.js
 *     messages.js
 *     messages/en.js
 *     messages/de.js
 *
 * for every app passed on the command line (defaults: web, dx-viewer).
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const root = resolve(import.meta.dir, '..');
const targets = process.argv.slice(2).length > 0 ? process.argv.slice(2) : ['apps/web', 'apps/dx-viewer'];

const en = JSON.parse(readFileSync(resolve(root, 'packages/i18n/messages/en.json'), 'utf-8'));
const de = JSON.parse(readFileSync(resolve(root, 'packages/i18n/messages/de.json'), 'utf-8'));

const keys = [...new Set([...Object.keys(en), ...Object.keys(de)])].sort();

function paramNames(str) {
	const out = new Set();
	for (const m of str.matchAll(/\{(\w+)\}/g)) out.add(m[1]);
	return [...out];
}

/**
 * Escape a string for embedding inside a JS template literal:
 * backslashes, backticks, and `${` must all be escaped to survive untouched
 * after the runtime evaluates the template.
 */
function escapeTemplate(value) {
	return value.replace(/[\\`]/g, (c) => '\\' + c).replace(/\$\{/g, '\\${');
}

function compileMessageBody(value) {
	const params = paramNames(value);
	if (params.length === 0) {
		return `(p) => \`${escapeTemplate(value)}\``;
	}
	// Replace {name} with ${p.name} after the surrounding text is escaped, so
	// translator-supplied `${...}` literals don't get evaluated.
	const escaped = escapeTemplate(value);
	const body = escaped.replace(/\{(\w+)\}/g, (_, name) => `\${p.${name}}`);
	return `(p) => \`${body}\``;
}

function writeMessagesFile(filepath, dict) {
	const lines = ['/* eslint-disable */'];
	for (const key of keys) {
		const value = dict[key] ?? en[key] ?? key;
		lines.push(`export const ${key} = ${compileMessageBody(String(value))}`);
	}
	mkdirSync(dirname(filepath), { recursive: true });
	writeFileSync(filepath, lines.join('\n') + '\n');
}

function writeBarrel(filepath) {
	const lines = [
		'/* eslint-disable */',
		'import { languageTag } from "./runtime.js"',
		'import * as en from "./messages/en.js"',
		'import * as de from "./messages/de.js"',
		''
	];
	for (const key of keys) {
		lines.push(`export const ${key} = (params = {}, options = {}) => {`);
		lines.push(`\treturn { en: en.${key}, de: de.${key} }[options.languageTag ?? languageTag()](params)`);
		lines.push('}');
		lines.push('');
	}
	mkdirSync(dirname(filepath), { recursive: true });
	writeFileSync(filepath, lines.join('\n'));
}

function writeRuntime(filepath) {
	const content = `/* eslint-disable */
export const sourceLanguageTag = "en"
export const availableLanguageTags = /** @type {const} */ (["en","de"])

// The current tag is either a static string or a resolver function — both are
// re-evaluated on every languageTag() call so dynamic sources (navigator.language,
// route-derived locale) stay current.
let _languageTagSource = sourceLanguageTag
const _onSetLanguageTag = new Set()

export const languageTag = () =>
	typeof _languageTagSource === "function" ? _languageTagSource() : _languageTagSource

export const setLanguageTag = (tag) => {
	_languageTagSource = tag
	const resolved = languageTag()
	for (const fn of _onSetLanguageTag) fn(resolved)
}

export const onSetLanguageTag = (fn) => { _onSetLanguageTag.add(fn) }
export const isAvailableLanguageTag = (tag) => availableLanguageTags.includes(tag)
`;
	mkdirSync(dirname(filepath), { recursive: true });
	writeFileSync(filepath, content);
}

function writeIgnore(filepath) {
	mkdirSync(dirname(filepath), { recursive: true });
	writeFileSync(filepath, '# auto-generated by scripts/regenerate-paraglide.mjs\n*\n');
}

for (const app of targets) {
	const base = resolve(root, app, 'src/lib/paraglide');
	writeRuntime(resolve(base, 'runtime.js'));
	writeBarrel(resolve(base, 'messages.js'));
	writeMessagesFile(resolve(base, 'messages/en.js'), en);
	writeMessagesFile(resolve(base, 'messages/de.js'), de);
	writeIgnore(resolve(base, '.gitignore'));
	writeIgnore(resolve(base, '.prettierignore'));
	console.log(`paraglide → ${app}/src/lib/paraglide (${keys.length} keys)`);
}
